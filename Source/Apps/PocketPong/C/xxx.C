void main()
{
	InitOS();
	ClearScreen();
	PutSprite(0, 0, field);
	ShowScreen();
	
	x1 = ABS_MIN + 5;
	y1 = 70;
	x2 = ABS_MAX - 5;
	y2 = 70;
	bx = 100;
	by = 100;
	dx = -2;
	dy = 0;
	score1 = 0;
	score2 = 0;
	
	done = 0;
	
	while (!done) {
		PutSprite(0, 0, field);

		TabletRead();
		ty = TabletGetY();
		if ( TabletGetX() > 140 && TabletGetY() > 160 )
			done = 1;
		if ( ty > 0 && ty < 160 )
		{
			if ( ty < y1 )
			{
				y1 --;
			}
			else if ( ty > y1 )
			{
				y1 ++;
			}
		}

		lever = getleverpush();
		
		switch ( lever ) {
			case 1 : 
				y1 --;
				break;
			case 3 :
				y1 ++;
				break;
		}

		if ( dx > 0 )
		{
			if ( y2 < by )
				y2 ++;
			else
				if ( y2 > by )
					y2 --;
		}
		else
		{
			if ( y2 < MIDDLE )
				y2 ++;
			else
				if ( y2 > MIDDLE )
					y2 --;
		}

		if ( y2 < MIN_Y+PAD_SIZE/2 )
			y2 = MIN_Y+PAD_SIZE/2;
		if ( y2 > MAX_Y-PAD_SIZE/2 )
			y2 = MAX_Y-PAD_SIZE/2;
		if ( y1 < MIN_Y+PAD_SIZE/2 )
			y1 = MIN_Y+PAD_SIZE/2;
		if ( y1 > MAX_Y-PAD_SIZE/2 )
			y1 = MAX_Y-PAD_SIZE/2;
		
		if ( bx+dx-4 <= x1 )
		{
			correct_ball( y1, x1+6 );
		}
	
		if ( bx+dx+4 >= x2 )
		{
			correct_ball( y2, x2-6 );
		}
		
		
		if ( by < MIN_Y+4 || by > MAX_Y-4 )
		{
			Sound( 1000, 10, 30 );
			dy =- dy;
		}

		if ( bx < ABS_MIN+3 || bx > ABS_MAX-3 )
		{
			if ( bx > 80 )
				score1 ++;
			else
				score2 ++;
			Note( "D5", 5000, 30 );
			dx = -dx;
		}
		if ( score1 > 9 || score2 > 9 )
			done = 1;
		
		bx += dx;
		by += dy;
		sprintf( buf, "%d:%d", score1, score2 );
		ShowText( 70, 26, buf );
		PutSprite( x1-4, y1-PAD_SIZE/2, pad);
		PutSprite( x2-4, y2-PAD_SIZE/2, pad);
		PutSpriteOP( bx-4, by-4, ball, PUT_OR );
		ShowScreen();
		if ( !lever )
			for(delay=0; delay<1000; delay++) {}
	}
	
	if ( score1 == 10 )
	{
		Message("Congratulations! You win.");
	}
	else if ( score2 == 10 )
	{
		Message("LOOSER :)");
	}
	
	Message("    PocketPong\r\rby Jordan Tuzsuzov\rwww.mordred.8m.com\r");
	CloseProgram();
}